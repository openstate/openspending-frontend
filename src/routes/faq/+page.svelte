			<div class="accordion-body">
				Dit kan of komen doordat de desbetreffende gemeente deze data niet heeft ontsloten en
				beschikbaar gesteld of de gemeente heeft ze wel beschikbaar gesteld maar daarin niet de
				categorieën ingevuld. Artikel 71 van het Besluit begroting en verantwoording provincies en
				gemeenten (BBV) bepaalt dat gemeenten verplicht zijn een overzicht van de begrote baten en
				lasten volgens de functionele indeling te leveren. Echter, gemeenten zijn niet verplicht
				daarbij een verdelingsmatrix te leveren aan het CBS. Voor wat betreft de realisaties moeten
				gemeenten en provincies wel elk kwartaal en elk jaar een verdelingsmatrix sturen met daarbij
				een balansstandenoverzicht. openspending.nl maakt gebruik van deze data. Wel is het zo dat
				de uitgaven van gemeenten alleen vergeleken kunnen worden met een begroting waarvan de
				categorieën zijn ingevuld.
